-- Justice and Mercy
UPDATE public.reading_plans
SET day4_context = 'Day 3 examined mercy. Today we explore their intersection: how can God be both just and merciful simultaneously?

This is the central problem of salvation. God''s justice demands punishment for sin; His mercy desires to forgive. How can He maintain both? At the cross, justice and mercy kissed (Psalm 85:10).

Jesus bore the full penalty for sin—justice was satisfied. Because the penalty was paid, mercy could be extended—without compromising justice. "God presented Christ as a sacrifice of atonement... He did it to demonstrate his righteousness... so as to be just and the one who justifies those who have faith in Jesus" (Romans 3:25-26). Today we worship the God who found a way to be both just and merciful.'
