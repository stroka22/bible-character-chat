-- Covenants of Bible
UPDATE public.reading_plans
SET day3_context = 'Day 1 introduced covenants as God''s way of relating to His people. Day 2 examined the covenant with Noah after the flood. Today we explore the Abrahamic covenant—the foundation for all that follows.

God called Abram from Ur and made stunning promises: land, descendants as numerous as stars, and blessing that would extend to all nations (Genesis 12:1-3, 15:5, 22:18). God formalized this in a covenant ceremony where He alone passed between the pieces—taking the curse of covenant-breaking upon Himself (Genesis 15).

