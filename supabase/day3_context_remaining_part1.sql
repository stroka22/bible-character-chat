-- Authority of Scripture
UPDATE public.reading_plans
SET day3_context = 'Day 1 introduced the doctrine of Scripture. Day 2 examined the Bible''s inspiration—God breathed through human authors. Today we explore inerrancy—the Bible''s complete truthfulness.

Inerrancy means Scripture, in its original manuscripts, is without error in all it affirms. This includes historical and scientific statements, not just theological ones. Jesus treated Scripture this way: "Scripture cannot be broken" (John 10:35). He trusted historical details—Jonah in the fish, Noah''s flood, Adam and Eve''s creation.

This doesn''t mean we always understand Scripture correctly—interpretation requires care. Nor does it mean the Bible uses modern scientific language—it describes the sun "rising" as we still do. But it does mean we can trust what Scripture teaches as God''s reliable Word. Today we examine what inerrancy means and why it matters for faith and life.'
