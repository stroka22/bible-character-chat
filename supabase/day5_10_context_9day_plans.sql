Peacemaking isn''t passivity or conflict avoidance. It''s actively pursuing reconciliation—between people and God, and between people and people. Jesus is the ultimate peacemaker: "He himself is our peace, who has made the two groups one and has destroyed the barrier" (Ephesians 2:14).

Peacemakers are called "children of God" because they share the family trait. God made peace through the cross; His children extend that peace. Today we examine: Where might God be calling you to make peace? What reconciliation have you avoided?'
