UPDATE public.reading_plans
SET day7_context = 'Day 6 explored showing mercy. Today we examine the sixth beatitude: "Blessed are the pure in heart, for they will see God" (Matthew 5:8).

Purity of heart isn''t sinlessness but singleness—an undivided heart wholly devoted to God. "Create in me a pure heart, O God" (Psalm 51:10). The impure heart serves multiple masters; the pure heart serves One.

The reward is staggering: seeing God. "Now we see only a reflection as in a mirror; then we shall see face to face" (1 Corinthians 13:12). Purity of heart positions us for the ultimate blessing—beholding God Himself. Today we ask: What divides my heart? What competes for my singular devotion to God?'
