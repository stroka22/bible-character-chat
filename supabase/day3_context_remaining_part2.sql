-- Grief and Comfort 14 Days
UPDATE public.reading_plans
SET day3_context = 'Day 1 acknowledged grief''s reality. Day 2 examined God as the God of all comfort. Today we explore lament—the biblical practice of honest grief expression.

Over one-third of the Psalms are laments. They don''t pretend everything is fine; they pour out pain honestly to God. "How long, LORD? Will you forget me forever?" (Psalm 13:1). "My God, my God, why have you forsaken me?" (Psalm 22:1). This isn''t lack of faith—it''s faith expressed in honesty.

Lament has structure: address to God, complaint, confession of trust, petition, and often a vow of praise. It takes pain to God rather than away from Him. Today we learn that grief fully expressed to God becomes grief that heals.'
