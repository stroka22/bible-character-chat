-- Grace vs Works
UPDATE public.reading_plans
SET day3_context = 'Day 1 introduced the grace-works question. Day 2 established justification by grace through faith. Today we explore sanctification—if we''re saved by grace, what role do works play?

Works don''t contribute to our justification but they evidence it. James wrote, "Faith without deeds is dead" (James 2:26). This doesn''t contradict Paul''s teaching on grace; James is addressing those who claim faith but show no fruit. Genuine faith produces good works.

Sanctification involves our effort—Paul says "work out your salvation with fear and trembling"—but only because "it is God who works in you" (Philippians 2:12-13). We work because God is working. Today we explore this dynamic partnership between divine grace and human response in the Christian life.'
