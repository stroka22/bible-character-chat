-- Forgiveness in Relationships
UPDATE public.reading_plans
SET day3_context = 'Day 1 introduced the necessity of forgiveness. Day 2 examined barriers to forgiving. Today we explore reconciliation—how forgiveness relates to restored relationship.

Forgiveness and reconciliation are related but distinct. Forgiveness is one-sided—you can forgive someone who never apologizes. Reconciliation requires two parties willing to rebuild trust. God calls us to always forgive; reconciliation depends on the other person''s response.

Some relationships can be fully restored after forgiveness. Others require boundaries because the person remains unsafe. Wisdom discerns the difference. Today we explore navigating these distinctions—offering forgiveness freely while protecting appropriately.'
