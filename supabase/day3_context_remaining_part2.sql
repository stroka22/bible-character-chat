UPDATE public.reading_plans
SET day3_context = 'Day 1 acknowledged depression''s reality. Day 2 examined Psalm 42-43—honest expression of despair. Today we explore practical biblical strategies for dark seasons.

Depression often involves negative thought patterns that Scripture addresses. Paul wrote, "Whatever is true, whatever is noble, whatever is right... think about such things" (Philippians 4:8). This isn''t positive thinking that denies reality, but redirecting thoughts toward truth.

Practical steps include: maintaining spiritual practices even when they feel empty; engaging community rather than isolating; caring for physical health (sleep, exercise, nutrition); and seeking professional help when needed. Depression isn''t spiritual failure—even prophets like Elijah and Jeremiah experienced it. Today we explore hope that sustains through darkness.'
