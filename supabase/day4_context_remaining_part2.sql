-- Gospels 30 Days
UPDATE public.reading_plans
SET day4_context = 'Day 3 examined reading the Gospels devotionally. Today we explore reading them historically: understanding the first-century context.

Jesus lived in a specific time and place: Roman-occupied Palestine, under Jewish law, amid various religious parties (Pharisees, Sadducees, Essenes, Zealots). Understanding this context illuminates the Gospels.

Why did Jesus antagonize Pharisees? They emphasized oral tradition alongside Scripture. Why were tax collectors despised? They collaborated with Rome and often cheated. What was significant about touching lepers or speaking with Samaritan women? Social boundaries were being crossed. Today we explore how historical context enriches Gospel reading.'
