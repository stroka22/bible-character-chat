-- Grace vs Works
UPDATE public.reading_plans
SET day4_context = 'Day 3 explored sanctification''s role. Today we examine the relationship between law and grace: if we''re under grace, what place does the law have?

Paul asked the same question: "Shall we go on sinning so that grace may increase? By no means!" (Romans 6:1-2). Grace doesn''t mean law doesn''t matter; it means law is no longer the basis for our standing with God. We obey out of gratitude and love, not to earn salvation.

The law reveals God''s character and guides righteous living. Grace enables what law demands. We''re no longer under law''s condemnation (Romans 8:1) but we uphold its moral principles through Spirit-empowered living. Today we explore this nuanced relationship between law and grace.'
