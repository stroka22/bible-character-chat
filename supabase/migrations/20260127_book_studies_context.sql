-- ISAIAH HIGHLIGHTS (14 days)
-- ============================================

UPDATE public.reading_plan_days 
SET context = 'Isaiah''s call came "in the year that King Uzziah died"—a moment of national crisis. The good king was gone; uncertainty loomed.

But Isaiah saw something greater than political upheaval: "I saw the Lord, high and exalted, seated on a throne; and the train of his robe filled the temple."

Human thrones may fall; God''s throne stands. Earthly kings die; the Lord reigns forever. This vision reoriented Isaiah''s perspective permanently.

"Above him were seraphim, each with six wings: With two wings they covered their faces, with two they covered their feet, and with two they were flying."

Even these exalted beings couldn''t bear God''s full glory. They covered their faces in His presence.

"Holy, holy, holy is the LORD Almighty; the whole earth is full of his glory."

The only divine attribute repeated three times in Scripture: holiness. Not "love, love, love" or "power, power, power"—holy, holy, holy. This is God''s essential nature, His defining characteristic.

"Woe to me! I am ruined! For I am a man of unclean lips, and I live among a people of unclean lips, and my eyes have seen the King, the LORD Almighty."

God''s holiness exposes human sinfulness. Isaiah didn''t feel good about himself in God''s presence; he felt undone.

Then grace: a seraph brought a live coal from the altar, touched Isaiah''s lips, and declared, "Your guilt is taken away and your sin atoned for."

Then the call: "Whom shall I send? And who will go for us?"

Isaiah''s response: "Here am I. Send me!"

True commission comes after true vision. See God''s holiness, experience His grace, then respond to His call.'
