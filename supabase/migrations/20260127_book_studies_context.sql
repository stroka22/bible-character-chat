-- GENESIS (21 days)
-- ============================================

UPDATE public.reading_plan_days 
SET context = '"In the beginning God created the heavens and the earth."

The Bible assumes God; it doesn''t argue for Him. Before anything else existed, God was. The universe has a beginning; God doesn''t.

"The earth was formless and empty, darkness was over the surface of the deep, and the Spirit of God was hovering over the waters."

Chaos before creation—but God''s Spirit was present, ready to bring order. This is the pattern throughout Scripture: God speaks into chaos and creates cosmos.

"And God said, ''Let there be light,'' and there was light."

Creation by word. God speaks and reality obeys. His word has power—effortless, absolute power.

The six days of creation move from formlessness to form, from emptiness to fullness. Light, sky, land and vegetation, sun and moon and stars, birds and fish, animals and finally humanity.

"So God created mankind in his own image, in the image of God he created them; male and female he created them."

Humans are unique. We share biological features with animals, but we bear God''s image—a spiritual reality that separates us from the rest of creation. This gives human life its inherent dignity.

"God saw all that he had made, and it was very good."

Creation was good—not fallen, not corrupt, not evil. What went wrong came later, through human choice. The world we know isn''t the world God made.

"By the seventh day God had finished the work he had been doing; so on the seventh day he rested."

Rest isn''t laziness; it''s divine pattern. God modeled what humans need—rhythm of work and rest, six and one, productivity and renewal.'
