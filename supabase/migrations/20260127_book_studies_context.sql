"In the past God spoke to our ancestors through the prophets at many times and in various ways, but in these last days he has spoken to us by his Son."

God has always communicated, but now He speaks definitively through the Son. The prophets were servants; Jesus is the Son.

"The Son is the radiance of God''s glory and the exact representation of his being, sustaining all things by his powerful word."

"Radiance of God''s glory"—just as light radiates from the sun, the Son radiates the Father''s glory. "Exact representation"—the Greek is "charakter," the imprint a seal leaves. Jesus perfectly represents the Father.

"Sustaining all things by his powerful word"—the universe holds together because Jesus speaks. He didn''t just create; He maintains.

"After he had provided purification for sins, he sat down at the right hand of the Majesty in heaven."

Old Testament priests never sat down—their work was never finished. Jesus sat down because His work is complete. Purification is provided. The sacrifice never needs repeating.

"So he became as much superior to the angels as the name he has inherited is superior to theirs."

Even angels, impressive as they are, can''t compare to Christ. "To which of the angels did God ever say, ''You are my Son''?"

The argument builds chapter by chapter: Jesus is better. Don''t go back to shadows when you have the reality.'
