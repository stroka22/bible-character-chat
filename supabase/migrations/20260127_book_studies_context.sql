-- EPHESIANS (10 days)
-- ============================================

UPDATE public.reading_plan_days 
SET context = 'Ephesians divides neatly: chapters 1-3 describe who we are in Christ; chapters 4-6 describe how to live accordingly. Doctrine precedes duty; identity precedes behavior.

"Praise be to the God and Father of our Lord Jesus Christ, who has blessed us in the heavenly realms with every spiritual blessing in Christ."

Paul opens with praise because of what God has done. "Every spiritual blessing"—not some, not most—every one.

"For he chose us in him before the creation of the world to be holy and blameless in his sight."

Before creation, God chose us. Election is controversial, but Paul states it plainly. Our salvation originates in God''s choice, not our decision.

"In love he predestined us for adoption to sonship through Jesus Christ."

"In love"—election isn''t cold decree but loving choice. "Adoption"—not just forgiveness but family membership with full inheritance rights.

"In him we have redemption through his blood, the forgiveness of sins, in accordance with the riches of God''s grace that he lavished on us."

"Redemption through his blood"—we were slaves to sin; Christ''s death purchased our freedom. "Lavished"—God doesn''t measure out grace sparingly; He pours it out.

"Having believed, you were marked in him with a seal, the promised Holy Spirit, who is a deposit guaranteeing our inheritance."

The Spirit is God''s seal of ownership and His deposit guaranteeing full inheritance. If you have the Spirit, you have God''s promise locked in.

This is who you are: chosen, adopted, redeemed, lavished with grace, sealed with the Spirit. Believe it. Live from it.'
