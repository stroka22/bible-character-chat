UPDATE public.reading_plan_days 
SET context = 'Genesis 3 is the hinge of history. Everything wrong with the world traces back to this chapter.

The serpent was "more crafty than any of the wild animals." Satan didn''t appear as a monster but as subtle questioner: "Did God really say...?"

The first attack was on God''s word. If Satan could raise doubt about what God said, disobedience would follow. This is still his strategy—question the Bible, undermine its authority, suggest God didn''t really mean it.

"You will not certainly die... For God knows that when you eat from it your eyes will be opened, and you will be like God, knowing good and evil."

The second attack was on God''s character. The serpent portrayed God as threatened by human potential, withholding blessing, lying about consequences. Eve began to see God as enemy rather than Father.

"When the woman saw that the fruit of the tree was good for food and pleasing to the eye, and also desirable for gaining wisdom, she took some and ate it."

The progression: seeing, desiring, taking. Sin rarely arrives full-blown. It starts with a look, moves to longing, ends in action.

"She also gave some to her husband, who was with her, and he ate it."

Adam was present. He said nothing, did nothing to protect or correct. His passivity was part of the failure.

"Then the eyes of both of them were opened, and they realized they were naked; so they sewed fig leaves together and made coverings for themselves."

The promised enlightenment came—but it was shame, not glory. They gained knowledge but lost innocence. They hid from each other and from God.

Every broken relationship, every disease, every death traces back to this moment. But God was already preparing the remedy.'
