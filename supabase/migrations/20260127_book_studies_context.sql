-- PSALMS (21 days)
-- ============================================

UPDATE public.reading_plan_days 
SET context = 'Psalm 1 is the gateway to the Psalter. It establishes the fundamental choice every person faces: the way of the righteous or the way of the wicked.

"Blessed is the one who does not walk in the counsel of the wicked or stand in the way that sinners take or sit in the company of mockers."

Notice the progression: walk, stand, sit. Sin begins with walking alongside the wrong influences, progresses to standing (stopping) with them, ends with sitting (settling in) among them. Casual exposure becomes comfortable participation.

"But whose delight is in the law of the LORD, and who meditates on his law day and night."

The alternative isn''t just avoiding evil but delighting in good. Negative avoidance isn''t enough; positive pursuit is required. And the focus of pursuit is God''s word—delighted in, meditated on, day and night.

"That person is like a tree planted by streams of water, which yields its fruit in season and whose leaf does not wither—whatever they do prospers."

Trees planted by streams don''t struggle for water. Their roots go deep, their fruit is reliable, their leaves stay green. This is the life that draws from God''s word—stable, fruitful, flourishing.

"Not so the wicked! They are like chaff that the wind blows away."

Chaff—worthless, weightless, blown away. No roots, no substance, no permanence. The contrast is stark.

"For the LORD watches over the way of the righteous, but the way of the wicked leads to destruction."

Two ways, two destinations. God knows and cares for one path; the other ends in ruin. The Psalms will explore both journeys, but Psalm 1 plants the signpost.'
