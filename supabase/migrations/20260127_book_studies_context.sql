-- ============================================
-- JAMES (7 days)
-- ============================================

UPDATE public.reading_plan_days 
SET context = 'James, written by Jesus'' brother, is the most practical book in the New Testament. It tells us how faith works in real life.

"Consider it pure joy, my brothers and sisters, whenever you face trials of many kinds."

Pure joy in trials—this seems impossible. But notice why:

"Because you know that the testing of your faith produces perseverance. Let perseverance finish its work so that you may be mature and complete, not lacking anything."

Trials have purpose. They test faith (revealing its genuineness), produce perseverance (endurance under pressure), and result in maturity (complete character). The process has a goal.

"If any of you lacks wisdom, you should ask God, who gives generously to all without finding fault, and it will be given to you."

Wisdom for trials is available. Ask God. He gives generously, not reluctantly. Without finding fault—He won''t criticize you for needing wisdom.

"But when you ask, you must believe and not doubt, because the one who doubts is like a wave of the sea, blown and tossed by the wind."

Ask with faith. Doubt produces instability—tossed around, no settled direction.

"Blessed is the one who perseveres under trial because, having stood the test, that person will receive the crown of life that the Lord has promised to those who love him."

Trials don''t last forever. Perseverance leads to blessing. The crown of life awaits those who endure.

James is blunt, practical, and demanding. He wants Christianity that works on Monday morning, not just Sunday.'
