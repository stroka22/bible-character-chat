-- ============================================

UPDATE public.reading_plan_days 
SET context = 'Exodus opens with Israel enslaved in Egypt. Four hundred years had passed since Joseph. A new Pharaoh "who did not know about Joseph" saw the Hebrews as a threat.

His solution: brutal oppression, then genocide. "Every Hebrew boy that is born you must throw into the Nile."

Into this horror, Moses was born. His mother hid him three months, then placed him in a basket among the reeds—committing him to God through apparent abandonment.

Pharaoh''s daughter found the baby and adopted him. The very household ordering Hebrew deaths now raised a Hebrew prince. God''s irony runs throughout Scripture.

Moses "was educated in all the wisdom of the Egyptians and was powerful in speech and action" (Acts 7:22). Palace training prepared him for palace confrontation. Nothing in his background was wasted.

At forty, Moses tried to help his people by killing an Egyptian oppressor. When discovered, he fled to Midian, where he spent the next forty years as a shepherd.

The prince became a fugitive became a shepherd. These seemed like wasted decades. But God was preparing Moses in the wilderness where he''d later lead Israel.

"The Israelites groaned in their slavery and cried out, and their cry for help because of their slavery went up to God. God heard their groaning and he remembered his covenant with Abraham, with Isaac and with Jacob."

God heard. God remembered. Not that He had forgotten, but He now moved to act. The timing was His, and it was perfect.'
