UPDATE public.reading_plan_days 
SET context = 'Psalm 51 is David''s response to his sin with Bathsheba. It''s the most profound prayer of repentance in Scripture.

"Have mercy on me, O God, according to your unfailing love; according to your great compassion blot out my transgressions."

David appeals to God''s character, not his own merit. He has no merit to appeal to. His only hope is God''s unfailing love and great compassion.

"Wash away all my iniquity and cleanse me from my sin."

Sin requires cleansing, not just forgiveness. It stains, pollutes, defiles. David needs washing.

"For I know my transgressions, and my sin is always before me. Against you, you only, have I sinned and done what is evil in your sight."

David doesn''t minimize. He sinned against Bathsheba and Uriah (whom he murdered), but ultimately all sin is against God. Sin is the violation of God''s character and commands.

"Surely I was sinful at birth, sinful from the time my mother conceived me."

Not just sinful acts but sinful nature. The problem isn''t just behavior but being.

"Create in me a pure heart, O God, and renew a steadfast spirit within me. Do not cast me from your presence or take your Holy Spirit from me."

David asks for internal transformation. He knows the problem is the heart. And he fears losing God''s presence—the worst possible consequence.

"The sacrifices of God are a broken spirit; a broken and contrite heart, O God, you will not despise."

God doesn''t want religious performance. He wants broken honesty. That, He will never reject.'
