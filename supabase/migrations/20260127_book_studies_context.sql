UPDATE public.reading_plan_days 
SET context = 'Isaiah 53 is the Bible''s clearest prophecy of Christ''s suffering—written 700 years before Jesus.

"He was despised and rejected by mankind, a man of suffering, and familiar with pain. Like one from whom people hide their faces he was despised, and we held him in low esteem."

The Messiah wouldn''t be a conquering hero but a despised sufferer. This wasn''t what Israel expected.

"Surely he took up our pain and bore our suffering, yet we considered him punished by God, stricken by him, and afflicted."

Here is substitutionary atonement. He bore our pain. He carried our suffering. We thought God was punishing Him; in reality, He was taking our punishment.

"But he was pierced for our transgressions, he was crushed for our iniquities; the punishment that brought us peace was on him, and by his wounds we are healed."

Pierced, crushed, punished—for us, not for Himself. The punishment that should have brought us judgment brought us peace instead. By His wounds—not our efforts—we are healed.

"We all, like sheep, have gone astray, each of us has turned to our own way; and the LORD has laid on him the iniquity of us all."

Universal diagnosis: all have strayed. Universal remedy: our iniquity laid on Him.

"He was led like a lamb to the slaughter, and as a sheep before its shearers is silent, so he did not open his mouth."

Silent submission. No protest, no complaint, no self-defense. This is what Jesus displayed before Pilate.

"For he was cut off from the land of the living; for the transgression of my people he was punished."

Cut off—killed—for the transgression of others. The innocent dying for the guilty. This is the gospel, seven centuries before the cross.'
