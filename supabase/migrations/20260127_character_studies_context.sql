The covenant still stands. Through Abraham''s offspring—ultimately Christ—all nations are blessed. We who believe are "children of Abraham" by faith (Galatians 3:7).

When God makes a promise, He keeps it. Even when the math doesn''t work. Even when years pass. Even when hope seems foolish.'
