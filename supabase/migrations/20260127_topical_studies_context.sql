"And hope does not put us to shame, because God''s love has been poured out into our hearts through the Holy Spirit, who has been given to us."

Hope won''t disappoint. Not because life always turns out as we want, but because God''s love is certain. The Spirit''s presence in our hearts guarantees the Father''s commitment to us.

