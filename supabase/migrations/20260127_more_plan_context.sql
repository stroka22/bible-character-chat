SET context = 'The Parable of the Wheat and Weeds addresses a problem we all face: why doesn''t God immediately destroy evil?

A farmer plants good wheat seed. An enemy secretly sows weeds—likely darnel, which looks exactly like wheat until harvest. The servants want to pull the weeds, but the master says no: "While you are pulling the weeds, you may uproot the wheat with them. Let both grow together until the harvest."

