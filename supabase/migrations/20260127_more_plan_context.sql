"The merciful" extend to others the grace they''ve received. Those forgiven much, forgive much.

"Pure in heart" means undivided loyalty—no competing allegiances, no hidden agendas.

"Peacemakers" don''t avoid conflict but pursue reconciliation at personal cost.

"Persecuted for righteousness" proves you''re living so differently that the world takes notice.

These aren''t separate categories of Christians. They''re characteristics of every genuine believer, developed by grace over time.'
