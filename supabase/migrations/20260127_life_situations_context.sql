Hebrews 12:5-11 compares God''s discipline to human parenting: "The Lord disciplines the one he loves, and he chastens everyone he accepts as his son."

Discipline proves love, not its absence. "If you are not disciplined... then you are not legitimate." Children without correction are neglected.

"No discipline seems pleasant at the time, but painful. Later on, however, it produces a harvest of righteousness and peace for those who have been trained by it."

The goal isn''t punishment but production—a harvest of righteousness. Discipline aims at character formation, not mere behavior modification.

Parents need wisdom: firm but not harsh, loving but not permissive, consistent but not rigid. Only God provides that balance.'
